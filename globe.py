class Quat:
    def __init__(self):
        self.w = 1.0
        self.x = 0.0
        self.y = 0.0
        self.z = 0.0
        self.m = [0.0] * 9
        self.identity()

    def identity(self):
        self.w = 1.0
        self.x = 0.0
        self.y = 0.0
        self.z = 0.0

    def norm(self):
        length = math.sqrt(self.w**2 + self.x**2 + self.y**2 + self.z**2)
        if length > 0.0:
            self.w /= length
            self.x /= length
            self.y /= length
            self.z /= length

    def rotate_by_axis(self, ax, ay, az, angle):
        half = angle * 0.5
        s = math.sin(half)
        dw = math.cos(half)
        dx = ax * s
        dy = ay * s
        dz = az * s
        
        nw = dw * self.w - dx * self.x - dy * self.y - dz * self.z
        nx = dw * self.x + dx * self.w + dy * self.z - dz * self.y
        ny = dw * self.y - dx * self.z + dy * self.w + dz * self.x
        nz = dw * self.z + dx * self.y - dy * self.x + dz * self.w
        
        self.w, self.x, self.y, self.z = nw, nx, ny, nz
        self.norm()

    def build_matrix(self):
        self.m[0] = 1.0 - 2.0 * (self.y**2 + self.z**2)
        self.m[1] = 2.0 * (self.x * self.y - self.z * self.w)
        self.m[2] = 2.0 * (self.x * self.z + self.y * self.w)
        self.m[3] = 2.0 * (self.x * self.y + self.z * self.w)
        self.m[4] = 1.0 - 2.0 * (self.x**2 + self.z**2)
        self.m[5] = 2.0 * (self.y * self.z - self.x * self.w)
        self.m[6] = 2.0 * (self.x * self.z - self.y * self.w)
        self.m[7] = 2.0 * (self.y * self.z + self.x * self.w)
        self.m[8] = 1.0 - 2.0 * (self.x**2 + self.y**2)

class Mouse:
    def __init__(self):
        self.px, self.py, self.pb = 0, 0, (False, False, False)
        self.mx, self.my, self.mb = 0, 0, (False, False, False)
        self.dx = 0.0
        self.dy = 0.0

    def poll(self, x, y, b):
        self.mx, self.my, self.mb = x, y, b
        self.dx = self.mx - self.px
        self.dy = self.my - self.py

    def dragging(self, button_idx):
        return self.mb[button_idx] and self.pb[button_idx]

    def commit(self):
        self.px = self.mx
        self.py = self.my
        self.pb = self.mb

class Globe:
    def __init__(self):
        self.tex = []
        self.tw = 0
        self.th = 0

    def load(self, data_list):
        self.tex = data_list
        self.tw = len(self.tex[0])
        self.th = len(self.tex)

    def render(self, q, zoom, pan_x, pan_y, surface):
        m0, m1, m2 = q.m[0], q.m[1], q.m[2]
        m3, m4, m5 = q.m[3], q.m[4], q.m[5]
        m6, m7, m8 = q.m[6], q.m[7], q.m[8]

        cx = COLS * 0.5 + pan_x
        cy = ROWS * 0.5 + pan_y
        rx = (COLS * 0.5) * FILL_PCT / 100.0 * zoom
        ry = (ROWS * 0.5) * FILL_PCT / 100.0 * zoom

        char_cache = {}

        for sy in range(ROWS):
            for sx in range(COLS):
                nx = (sx - cx) / rx if rx != 0 else 0
                ny = (sy - cy) / ry if ry != 0 else 0
                d = nx*nx + ny*ny

                if d <= 0.9999:
                    d2 = 1.0 - d
                    if d2 < 0.0: d2 = 0.0
                    nz = math.sqrt(d2)
                    if nz < 0.0001: nz = 0.0001

                    vx = m0*nx + m3*ny + m6*nz
                    vy = m1*nx + m4*ny + m7*nz
                    vz = m2*nx + m5*ny + m8*nz

                    if vy > 0.9999: vy = 0.9999
                    if vy < -0.9999: vy = -0.9999
                    denom = math.sqrt(1.0 - vy*vy)
                    if denom < 0.0001: denom = 0.0001
                    phi = 1.570796 - math.atan(-vy / denom)

                    if abs(vx) < 0.0001 and abs(vz) < 0.0001:
                        theta = 0.0
                    elif abs(vx) < 0.0001:
                        theta = 1.570796 if vz > 0.0 else 4.712389
                    else:
                        theta = math.atan(vz / vx)
                        if vx < 0.0: theta += 3.14159
                        if theta < 0.0: theta += 6.28318

                    tx = int((theta / 6.28318) * self.tw + 0.5)
                    ty = int((phi / 3.14159) * self.th + 0.5)

                    if tx < 1: tx = self.tw
                    if tx > self.tw: tx = 1
                    if ty < 1: ty = 1
                    if ty > self.th: ty = self.th

                    # Clamp row safely using total textures count
                    target_y = max(0, min(ty - 1, self.th - 1))
                    
                    # Dynamically obtain the target row string width to fully protect slice indices
                    row_len = len(self.tex[target_y])
                    target_x = max(0, min(tx - 1, row_len - 1))

                    c_str = self.tex[target_y][target_x]
                    color = COLOR_BLUE if c_str == "." else COLOR_BROWN

                    cache_key = (c_str, color)
                    if cache_key not in char_cache:
                        char_cache[cache_key] = font.render(c_str, True, color)
                    
                    surface.blit(char_cache[cache_key], (sx * CELLW, sy * CELLH))

