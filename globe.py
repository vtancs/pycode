# Objects setup
q = Quat()
ms = Mouse()
earth = Globe()
earth.load(map_data)

zoom = 1.0
pan_x = 0.0
pan_y = 0.0
is_rotating = True  # Tracks state of auto-rotation
running = True

while running:
    screen.fill(COLOR_BLACK)

    # Core Pygame Event Processing loop
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        elif event.type == pygame.KEYDOWN:
            if event.key == pygame.K_ESCAPE:
                running = False
            elif event.key == pygame.K_SPACE:
                is_rotating = not is_rotating  # Toggle rotation flag on spacebar click

    # Apply passive auto-rotation if activated
    if is_rotating:
        q.rotate_by_axis(0.0, 1.0, 0.0, 0.008)   # Continuous spin on Y axis
        q.rotate_or_axis = q.rotate_by_axis(1.0, 0.0, 0.0, 0.001)   # Slight tilt modulation

    # Standard Keyboard inputs checking state per frame
    keys = pygame.key.get_pressed()
    
    # Panning controls via Arrow keys
    if keys[pygame.K_LEFT]:
        pan_x += 0.5
    if keys[pygame.K_RIGHT]:
        pan_x -= 0.5
    if keys[pygame.K_UP]:
        pan_y += 0.5
    if keys[pygame.K_DOWN]:
        pan_y -= 0.5

    # Zooming controls via standard W/S keys
    if keys[pygame.K_w]:
        zoom += 0.02
    if keys[pygame.K_s]:
        zoom -= 0.02

    # Constrain values inside bounding zones
    if zoom < 0.3: zoom = 0.3
    if zoom > 3.0: zoom = 3.0

    # Process continuing Mouse actions
    mx, my = pygame.mouse.get_pos()
    mb = pygame.mouse.get_pressed(num_buttons=3)
    ms.poll(mx, my, mb)

    # Left click & drag: Sphere trackball rotation
    if ms.dragging(0):
        q.rotate_by_axis(0.0, 1.0, 0.0, ms.dx * 0.02)
        q.rotate_by_axis(1.0, 0.0, 0.0, -ms.dy * 0.02)

    # Right click & drag: Z-axis roll twist
    if ms.dragging(2):
        q.rotate_by_axis(0.0, 0.0, 1.0, ms.dx * 0.02)

    ms.commit()

    # Re-calculate geometry and push transformations to screen
    q.build_matrix()
    earth.render(q, zoom, pan_x, pan_y, screen)

    # Footer UI layout text render
    status_str = "RUNNING" if is_rotating else "STOPPED"
    info_text = f"SPACE: Turn Rotation {status_str}  |  Arrows: Pan  |  W/S: Zoom"
    info_surface = font.render(info_text, True, COLOR_WHITE)
    screen.blit(info_surface, (10, SCREEN_HEIGHT - 22))

    pygame.display.flip()
    clock.tick(FPS)

pygame.quit()
sys.exit()